import { ApiPropertyOptional } from '@nestjs/swagger';
import { NumberFieldOptional } from '../../../decorators';

export class GetAllDocumentPayload {
  @ApiPropertyOptional()
  @NumberFieldOptional({
    minimum: 0,
    default: 0,
    int: true,
  })
  readonly skip: number;

  @ApiPropertyOptional()
  @NumberFieldOptional({
    minimum: 1,
    default: 100,
    int: true,
  })
  readonly take: number;
}
